<?php
// Intelligently remove extra P and BR tags around shortcodes that WordPress likes to add
function wpex_fix_shortcodes($content){   
    $array = array (
        '<p>[' => '[', 
        ']</p>' => ']', 
        ']<br />' => ']'
    );

    $content = strtr($content, $array);
    return $content;
}
?>